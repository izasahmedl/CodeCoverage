namespace CodeCoverage;

/// <summary>
/// Class library to do calculation operations
/// </summary>
public class Calculator
{
    public Calculator()
    {

    }
    public int Add(int number1, int number2)
    {
        return number1 + number2;
    }

    public int Subtract(int number1, int number2)
    {
        return number1 - number2;
    }
}